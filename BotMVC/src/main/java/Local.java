
public class Local {
	
	private String name;
	private String instituicao;
	
	public Local(String name, String instituicao) {
		this.name = name;
		this.instituicao = instituicao;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getInstituicao() {
		return instituicao;
	}
	public void setEmail(String instituicao) {
		this.instituicao = instituicao;
	}	

}
